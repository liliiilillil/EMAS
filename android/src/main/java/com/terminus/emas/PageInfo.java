package com.terminus.emas;

public class PageInfo {
    String pageName = null;
    long time = 0;

    public String getPageName() {
        return pageName;
    }

    public long getTime() {
        return time;
    }

    public void setPageName(String pageName) {
        this.pageName = pageName;
    }

    public void setTime(long time) {
        this.time = time;
    }
}

